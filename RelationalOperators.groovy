class RelationalOperators {

    def a = 5
    def b = 10


// Equal to (==)
    def isEqual = (a == b)// false

// Not equal to (!=)
    def isNotEqual = (a != b) //true

// Grater than (>)
    def isGreaterThan = (a > b) //false

// Less Than (<)
    def isLessThan = (a < b) //true

// Grater than or equal to (>=)
    def isGreaterThanOrEqual = (a >= b) //false

// Less than or equal to (<=)
    def isLessThanOrEqual = (a <= b) //true


}

